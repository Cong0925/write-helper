// ====== 多色图标（彩色 Emoji） ======
function card(id: string, name: string, icon: string, emojis: string[], accent: string): TemplateItem {
  const items = emojis
    .map(
      (e) =>
        `<span data-emoji="${e}" style="display:inline-flex;align-items:center;justify-content:center;width:44px;height:44px;background:#f5f5fa;border-radius:10px;font-size:24px;cursor:pointer;">${e}</span>`,
    )
    .join('')
  return {
    id,
    name,
    category: 'emoji',
    subCategory: 'colorful',
    html: `<div style="width:100%;"><div style="padding:10px 14px;font-size:13px;font-weight:600;color:#444;border-bottom:2px solid ${accent};background:#fafbfc;display:flex;align-items:center;gap:6px;">${icon} ${name}</div><div style="padding:12px 14px;display:flex;flex-wrap:wrap;gap:8px;justify-content:center;">${items}</div></div>`,
  }
}

// ====== 颜文字（纯文字表情） ======
const esc = (s: string) => s.replace(/&/g, '&amp;').replace(/"/g, '&quot;').replace(/</g, '&lt;').replace(/>/g, '&gt;')

function kaomojiCard(id: string, name: string, icon: string, items: string[]): TemplateItem {
  const content = items
    .map(
      (item) =>
        `<span data-emoji="${esc(item)}" style="display:inline-block;padding:5px 9px;margin:3px;border-radius:6px;background:#f5f5fa;font-size:14px;cursor:pointer;white-space:nowrap;line-height:1.7;color:#444;">${item}</span>`,
    )
    .join('')
  return {
    id,
    name,
    category: 'emoji',
    subCategory: 'kaomoji',
    html: `<div style="width:100%;"><div style="padding:10px 14px;font-size:13px;font-weight:600;color:#555;border-bottom:2px solid #c084fc;background:#fafbfc;display:flex;align-items:center;gap:6px;">${icon} ${name}</div><div style="padding:10px 12px;display:flex;flex-wrap:wrap;align-items:center;">${content}</div></div>`,
  }
}

// ====== 单色图标（线条/轮廓符号） ======
function monoCard(id: string, name: string, icon: string, items: string[]): TemplateItem {
  const content = items
    .map(
      (item) =>
        `<span data-emoji="${esc(item)}" style="display:inline-flex;align-items:center;justify-content:center;width:44px;height:44px;background:#f0f0f2;border-radius:10px;font-size:22px;cursor:pointer;color:#555;">${item}</span>`,
    )
    .join('')
  return {
    id,
    name,
    category: 'emoji',
    subCategory: 'mono',
    html: `<div style="width:100%;"><div style="padding:10px 14px;font-size:13px;font-weight:600;color:#555;border-bottom:2px solid #94a3b8;background:#fafbfc;display:flex;align-items:center;gap:6px;">${icon} ${name}</div><div style="padding:12px 14px;display:flex;flex-wrap:wrap;gap:8px;justify-content:center;">${content}</div></div>`,
  }
}

