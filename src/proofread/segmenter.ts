// ─── Build extended vocabulary from error dictionary words ───
export function expandVocab(words: string[]): void {
  for (const w of words) {
    if (w.length > 0) VOCAB.add(w)
  }
}

/**
 * Segment Chinese text into words using Forward Maximum Matching.
 * Falls back to single characters for unknown words.
 */
export function segment(text: string): string[] {
  if (!text) return []
  const result: string[] = []
  let i = 0
  const maxLen = 6 // 最长词不超过 6 字

  while (i < text.length) {
    let matched: string | null = null
    // Try longest match first
    for (let len = Math.min(maxLen, text.length - i); len >= 1; len--) {
      const word = text.slice(i, i + len)
      if (VOCAB.has(word)) {
        matched = word
        break
      }
    }
    if (matched) {
      result.push(matched)
      i += matched.length
    } else {
      // Unknown — treat single char as a word
      result.push(text[i])
      i++
    }
  }
  return result
}
