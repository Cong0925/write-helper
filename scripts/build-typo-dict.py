# ═══════════════════════════════════════════════════════════════
# 1. Pycorrector custom_confusion.txt (tab-separated: wrong\tcorrect\t[weight])
# ═══════════════════════════════════════════════════════════════
for path in ['/tmp/confusion_raw.txt', os.path.join(BASE, 'scripts', 'confusion_raw.txt')]:
    if os.path.exists(path):
        with open(path, encoding='utf-8') as f:
            for line in f:
                line = line.strip()
                if not line or line.startswith('#'):
                    continue
                parts = line.split('\t')
                if len(parts) < 2:
                    parts = line.split()
                if len(parts) >= 2:
                    w, c = parts[0].strip(), parts[1].strip()
                    if w and c and w != c:
                        final[w] = c
        print(f'[1] Pycorrector: {len(final)} entries loaded from {path}')
        break

# ═══════════════════════════════════════════════════════════════
# 2. same_pinyin.txt → generate homophone confusion pairs
#    Format: #汉字\t同音同调\t同音异调
# ═══════════════════════════════════════════════════════════════
pinyin_count = 0
for pinyin_path in ['/tmp/same_pinyin.txt', os.path.join(BASE, 'scripts', 'same_pinyin.txt')]:
    if os.path.exists(pinyin_path):
        with open(pinyin_path, encoding='utf-8') as f:
            for line in f:
                line = line.strip()
                if not line or line.startswith('#'):
                    continue
                parts = line.split('\t')
                if len(parts) >= 2:
                    char = parts[0].strip()
                    if len(char) != 1:
                        continue
                    for block in parts[1:]:
                        for v in block:
                            if v and v != char and v not in final:
                                final[v] = char
                                pinyin_count += 1
        print(f'[2] same_pinyin.txt: {pinyin_count} pairs generated from {pinyin_path}')
        break

# ═══════════════════════════════════════════════════════════════
# 3. same_stroke.txt → generate similar-stroke confusion pairs
